#include <stdio.h>

int main()
{
	int a[10] = {1,2,3,4,5};
	
	//当一个指针指向某一个类型的数组时，实际上该指针保存了数组首个元素的首地址，即p=&a[0] 
	int *p = a;
	printf("*p = %d\n", *p);
	
	//此时p与a等价，即指针名p[i]可以当作数组名a[i]使用 
	p[9] = 10;
	a[1] = 22;
	//可以通过移动指针来引用数组元素 
	*(p+2) = 33;
	
	//p和a的不同：p是指针变量，a是常量，所以可以用等号给p赋值，但不能用等号给a赋值 
	*p = 11;
	//a = 111;
	
	int i;
	for(i=0;i<10;i++)
	{
		printf("a[%d] = %d\n", i,a[i]);
	}
	
	int *q = a;
	for(i=0;i<10;i++)
	{
		printf("*q = %d\n", *q);
		q++;
	}
	
	return 0;
}
